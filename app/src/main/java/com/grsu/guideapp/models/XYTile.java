package com.grsu.guideapp.models;

public class XYTile {

    private int XTile;
    private int YTile;

    public XYTile(int XTile, int YTile) {
        this.XTile = XTile;
        this.YTile = YTile;
    }

    public int getXTile() {
        return XTile;
    }

    public int getYTile() {
        return YTile;
    }
}
